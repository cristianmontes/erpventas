package com.wildc.ucumari.client.modelo;

import java.io.Serializable;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Embeddable;

/**
 *
 * @author Cristian
 */
@Embeddable
public class DocumentAttributePK implements Serializable {
    @Basic(optional = false)
    @Column(name = "DOCUMENT_ID")
    private String documentId;
    @Basic(optional = false)
    @Column(name = "ATTR_NAME")
    private String attrName;

    public DocumentAttributePK() {
    }

    public DocumentAttributePK(String documentId, String attrName) {
        this.documentId = documentId;
        this.attrName = attrName;
    }

    public String getDocumentId() {
        return documentId;
    }

    public void setDocumentId(String documentId) {
        this.documentId = documentId;
    }

    public String getAttrName() {
        return attrName;
    }

    public void setAttrName(String attrName) {
        this.attrName = attrName;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (documentId != null ? documentId.hashCode() : 0);
        hash += (attrName != null ? attrName.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof DocumentAttributePK)) {
            return false;
        }
        DocumentAttributePK other = (DocumentAttributePK) object;
        if ((this.documentId == null && other.documentId != null) || (this.documentId != null && !this.documentId.equals(other.documentId))) {
            return false;
        }
        if ((this.attrName == null && other.attrName != null) || (this.attrName != null && !this.attrName.equals(other.attrName))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.wildc.ucumari.client.modelo.DocumentAttributePK[ documentId=" + documentId + ", attrName=" + attrName + " ]";
    }
    
}
